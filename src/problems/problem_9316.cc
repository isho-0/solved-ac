#include "problem_9316.h"
#include <iostream>

void solve_problem_9316() {
    std::cout << "=== 9316번 Hello Judge 문제 해결 ===" << std::endl;

    int n;
    std::cin >> n;

    for (int i = 1; i <= n; i++) {
        std::cout << "Hello World, Judge " << i << "!\n";
    }

    std::cout << "================================" << std::endl;
}
